package Variables;

public class Array {
	
	private int value, size;

	public Array(int value, int size) {
		this.value = value;
		this.size = size;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public int getSize() {
		return size;
	}

	
	
}
